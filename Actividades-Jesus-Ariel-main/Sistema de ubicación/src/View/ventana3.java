    public void ListarContinentes() {
        List<Continentes> listarContinentes = cont.ListarContinentes();
        modelo = (DefaultTableModel) tablaContinetes.getModel();
        Object[] ob = new Object[3];
        for (int i = 0; i < listarContinentes.size(); i++) {
            ob[0] = listarContinentes.get(i).getId();
            ob[1] = listarContinentes.get(i).getCodigo_postal();
            ob[2] = listarContinentes.get(i).getDescripcion();
            modelo.addRow(ob);
        }
        tablaContinetes.setModel(modelo);
    }

    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

    public void Limpiar() {
        txtid.setText("");
        txtCodigo.setText("");
        txtdescripcion.setText("");

    }

