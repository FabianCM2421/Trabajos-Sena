    private void btnmodificaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificaActionPerformed
        int id;
        String codigo_postal;
        String descripcion;

        if ("".equals(txtid.getText())) {
            JOptionPane.showMessageDialog(null, "Seleecione una fila");
        } else {
            if (!"".equals(txtCodigo.getText()) || !"".equals(txtdescripcion.getText())) {
                cont.setCodigo_postal(txtCodigo.getText());

                id = Integer.parseInt(txtid.getText());
                codigo_postal = txtCodigo.getText();
                descripcion = txtdescripcion.getText();
                contCroller.Modificar(id, codigo_postal, descripcion);

                JOptionPane.showMessageDialog(null, " Modificado");
                LimpiarTable();
                Limpiar();
                ListarContinentes();
                btnmodifica.setEnabled(false);
                btnGuardar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, " No se pudeo Hcaer el Cambio");

            }
        }
    }//GEN-LAST:event_btnmodificaActionPerformed

    private void BtnEliminaFisicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminaFisicoActionPerformed

        int Id;

        if (!txtid.getText().isEmpty()) {
            int pregunta = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar?");
            if (pregunta == 0) {
                Id = Integer.parseInt(txtid.getText());
                contCroller.EliminarRegistroF(Id);
                LimpiarTable();
                Limpiar();
                ListarContinentes();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila.");
        }


    }//GEN-LAST:event_BtnEliminaFisicoActionPerformed

