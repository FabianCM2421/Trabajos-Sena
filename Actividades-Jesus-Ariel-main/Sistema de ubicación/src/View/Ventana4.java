    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        int id;
        String codigo_postal;
        String descripcion;
        String contiente_id;

        if ("".equals(txtid.getText())) {
            JOptionPane.showMessageDialog(null, "Seleecione una fila");
        } else {
            if (!"".equals(txtCodigo.getText()) || !"".equals(txtdescripcion.getText()) || !"".equals(txtpaisid.getText())) {
                ps.setCodigo_postal(txtCodigo.getText());

                id = Integer.parseInt(txtid.getText());
                codigo_postal = txtCodigo.getText();
                descripcion = txtdescripcion.getText();
                contiente_id = txtpaisid.getText();

                System.out.println("datos del view  " + contiente_id);

                // contiente_id = Integer.parseInt(txtpaisid.getText());
                PsC.Modificar(id, codigo_postal, descripcion, contiente_id);
                JOptionPane.showMessageDialog(null, " Modificado");
                LimpiarTable();
                Limpiar();
                ListarContinentes();
                btnmodificar.setEnabled(false);
                BTNGUARDAR.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, " No se pudeo Hcaer el Cambio");

            }
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

