    public void ListarContinentes() {
        List<Pais> ListarPaises = ps.ListarPaises();
        modelo = (DefaultTableModel) tablapaises.getModel();
        Object[] ob = new Object[4];
        for (int i = 0; i < ListarPaises.size(); i++) {
            ob[0] = ListarPaises.get(i).getId();
            ob[1] = ListarPaises.get(i).getCodigo_postal();
            ob[2] = ListarPaises.get(i).getDescripcion();
            ob[3] = ListarPaises.get(i).getIdContinente();
            modelo.addRow(ob);
        }
        tablapaises.setModel(modelo);
    }

    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

    public void Limpiar() {
        txtid.setText("");
        txtCodigo.setText("");
        txtdescripcion.setText("");
        txtpaisid.setText("");

    }

